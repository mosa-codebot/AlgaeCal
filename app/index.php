            function isOfficeHours(officeHours){
                var date = new Date();
                var day = date.getDay();
                var time = parseInt(date.getHours()+""+date.getMinutes());
                var currentOfficeDay;
                $.each(officeHours, function(index, value){
                    if (day == value.day){
                        currentOfficeDay = value;
                    }
                });
                if((time > parseInt(currentOfficeDay.starting_time)) && (time <  parseInt(currentOfficeDay.closing_time))){
                    return true;
                }
                return false;
            }
             
